package me.cybermaxke.elementarrows.forge.network;

import io.netty.buffer.ByteBuf;

import cpw.mods.fml.common.network.ByteBufUtils;
import cpw.mods.fml.common.network.simpleimpl.IMessage;
import cpw.mods.fml.relauncher.Side;
import cpw.mods.fml.relauncher.SideOnly;

public final class MessageModInfo implements IMessage {
	public String version;

	public MessageModInfo() {

	}

	@SideOnly(Side.CLIENT)
	public MessageModInfo(String version) {
		this.version = version;
	}

	@Override
	public void fromBytes(ByteBuf buf) {
		this.version = ByteBufUtils.readUTF8String(buf);
	}

	@Override
	public void toBytes(ByteBuf buf) {
		ByteBufUtils.writeUTF8String(buf, this.version);
	}

}